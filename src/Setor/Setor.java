package Setor;

public class Setor {
	private String name;
	private String andar;

	public Setor() {

	}

	public Setor(String name, String andar) {
		this.name = name;
		this.andar = andar;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAndar() {
		return andar;
	}

	public void setAndar(String andar) {
		this.andar = andar;
	}

}
